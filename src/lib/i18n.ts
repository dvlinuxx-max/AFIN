export function dirFor(locale: Locale): "rtl" | "ltr" {
  return locale === "ar" ? "rtl" : "ltr";
}

export function normalizeLocale(value: string | undefined | null): Locale {
  return value === "en" ? "en" : "ar";
}

// Pick the localized string from a bilingual pair, falling back to the other language.
export function bi(pair: { ar: string; en: string } | undefined, locale: Locale): string {
  if (!pair) return "";
  return (locale === "ar" ? pair.ar : pair.en) || pair.ar || pair.en || "";
}

